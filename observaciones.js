function habilitarObservaciones() {
  var personaSI = document.querySelector('input[name="persona"][value="SI"]');
  var observaciones = document.getElementById("observaciones");

  if (personaSI.checked) {
    observaciones.disabled = false;  // Habilita el campo de texto si se selecciona "SI"
  } else {
    observaciones.disabled = true;   // Deshabilita el campo de texto si se selecciona "NO"
    observaciones.value = "";        // Limpia el contenido si se selecciona "NO"
  }
}

